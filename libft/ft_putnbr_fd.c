#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	r;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	r = nb % 10 + '0';
	write(fd, &r, 1);
}
